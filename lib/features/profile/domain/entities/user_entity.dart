class UserEntity {
  final String username;
  final String fullName;
  final String status;
  final String email;
  final String phone;
  final String address;
  final int salary;

  const UserEntity({
    required this.username,
    required this.fullName,
    required this.status,
    required this.email,
    required this.phone,
    required this.address,
    required this.salary,
  });
}
